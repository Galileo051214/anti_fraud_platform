def add_heading_custom(text, style_name):
    """添加标题并尝试匹配原模板样式"""
    p = doc.add_paragraph()
    run = p.add_run(text)
    # 尝试从模板样式获取字体设置
    try:
        style = doc.styles[style_name]
        font = style.font
        run.font.name = font.name
        run.font.size = font.size
        run.font.bold = font.bold
        if font.color and font.color.rgb:
            run.font.color.rgb = font.color.rgb
        # 复制中文字体设置
        rPr = run._element.get_or_add_rPr()
        rFonts = rPr.find(qn('w:rFonts'))
        if rFonts is not None:
            run._element.rPr.rFonts.set(qn('w:eastAsia'), rFonts.get(qn('w:eastAsia'), '宋体'))
    except Exception:
        pass
    p.style = style_name
    return p


def add_normal_paragraph(text, bold=False):
    """添加正文段落"""
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = '宋体'
    run._element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
    run.font.size = Pt(12)
    run.font.bold = bold
    p.paragraph_format.first_line_indent = Inches(0.4)
    p.paragraph_format.line_spacing = 1.5
    return p


def add_bullet_paragraph(text):
    """添加列表项段落"""
    p = doc.add_paragraph()
